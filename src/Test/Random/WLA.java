package Test.Random;

public class WLA {
    public static void main(String[] args) {
        //while up
        int i = 0;
        while (i < 10) {
            System.out.println("messi is ok");
            i++;
        }
        System.out.println("---------------");
        //while down
        i = 10;
        while (i > 0) {
            System.out.println("messi is ok");
            i--;
        }
        System.out.println("---------------");
        //for up
        for (i = 0; i < 10; i++) {
            System.out.println("messi is ok");
        }
        System.out.println("---------------");
        //for down
        for (i = 10; i > 0; i--) {
            System.out.println("messi is ok");
        }
        System.out.println("---------------");


    }
}
